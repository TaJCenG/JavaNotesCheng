package javaTajCheng;

public  class eSwitchState {

	public static void main(String[] args) {
		
		int a = 1;
		 if (a==1){
			 System.out.println("one");
		 }
		 else if (a == 2) {
			 System.out.println("two");
		 }
		 System.out.println("----------------------------------------------------------------------");		 
		 int b = 2;
		 
		 switch (b)
		 {
		 
		 case 1:
			 System.out.println("one");
		        break;
		 case 2 :
			 System.out.println("two");
		default :
			System.out.println("Hi bro");
		 
	}

	}}
