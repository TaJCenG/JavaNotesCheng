package OOPSDay14;

public class DepartmentEmployee {
	
	private Integer id;
	private String Name;
	public DepartmentEmployee(Integer id, String name) {
		super();
		this.id = id;
		this.Name = name;
	}
	public Integer getId() {
		return id;
	}
	public String getName() {
		return Name;
	}
	

}
